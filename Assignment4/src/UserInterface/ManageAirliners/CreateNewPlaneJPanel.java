    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        //txtAirlinerName.setText(airliner.getAirlinerName());
      
        this.airPlane=airliner.addAirplane();
        int seatRows = Integer.parseInt(txtSeatRows.getText());
        int seatColumns = Integer.parseInt(txtSeatColumns.getText());
        String planeId=txtPlaneId.getText();
        for(Airplane a:airliner.getFleet() ){
             if(planeId.equals(a.getPlaneId())){
                 JOptionPane.showMessageDialog(null, "Plane already exists");
            return;
             }
        }
       
       
        if (txtSeatRows.getText().equals("") || txtSeatColumns.getText().equals("") || planeId.equals("") ) {
            JOptionPane.showMessageDialog(null, "All fields are mandatory");
            return;
        }
        else{
            airPlane.setAirlinerName(airliner.getAirlinerName());
            airPlane.setPlaneId(planeId);
            airPlane.setSeatRows(seatRows);
            airPlane.setSeatCols(seatColumns);
            
            JOptionPane.showMessageDialog(null, "Airplane created successfully");
          
            
        }
    }//GEN-LAST:event_btnCreateActionPerformed

